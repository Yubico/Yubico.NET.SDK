namespace Yubico.YubiKey.Piv
{
    /// <summary>
    /// This enum lists the possible status values of a key in a PIV slot.
    /// </summary>
    /// <remarks>
    /// The PIN, PUK, or management key can be default, an asymmetric key can be
    /// imported or generated. If there is no key in the slot the status is
    /// Unknown.
    /// </remarks>
    public enum PivKeyStatus
    {
        /// <summary>
        /// If there is no key in a slot, its status is Unknown.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// A private key was generated by the YubiKey in the secure element.
        /// </summary>
        Generated = 1,

        /// <summary>
        /// A private key was imported from off the YubiKey.
        /// </summary>
        Imported = 2,

        /// <summary>
        /// A PIN, PUK, or management key can have the default value<br/>
        /// (PIN is "123456", PUK is "12345678", management key is
        /// 0x0102030405060708 three times).
        /// </summary>
        Default = 3,

        /// <summary>
        /// The PIN, PUK, or management key is not the default value.<br/>
        /// It is not possible to have the YubiKey report the actual value.
        /// </summary>
        NotDefault = 4,
    }
}
